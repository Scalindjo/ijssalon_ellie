def mijn_functie_1(a):
    uitkomst = a * a
    return uitkomst
print(mijn_functie_1(12))

def mijn_functie_2(a, b):
    optellen = a+b
    aftrekken = a-b
    vermenigvuldigen = a*b
    delen = a/b
    uitkomst = [optellen, aftrekken, vermenigvuldigen, delen]
    return uitkomst
print(mijn_functie_2(12,3))

